part of 'extensions.dart';

extension DateTimeExtension on DateTime {
  String get dateAndTime => '$shortDayName $day, $hour:00';
  String get shortDayName {
    switch (weekday) {
      case 1:
        return 'Mon';
      case 2:
        return 'Tue';
      case 3:
        return 'Wed';
      case 4:
        return 'Thu';
      case 5:
        return 'Fri';
      case 6:
        return 'Sat';
      case 7:
        return 'Sun';
      default:
        return '';
    }
  }
}
